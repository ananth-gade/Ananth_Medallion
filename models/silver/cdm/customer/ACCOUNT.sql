,CTE_SFDC AS (
    SELECT 
        ACCOUNTNUMBER,
        INFOPRO_ACCOUNT_NUMBER__C,
        INFOPRO_NUMBER__C,  
        MY_RESOURCE_CUSTOMER__C, 
