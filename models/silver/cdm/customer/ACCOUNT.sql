{% if is_incremental() %}
, CTE_CHANGED_ACCOUNTS AS (
    -- ARPCU changes
    SELECT DISTINCT CUCO AS INFOPRO_DIVISION_NUMBER, CUCUNO AS ACCOUNT_NUMBER
    FROM {{ ref('ODS_IFP_ARPCU') }}
    WHERE ODS_UPDATE_TIMESTAMP > (SELECT MAX(t.UPDATE_TIMESTAMP_ODS_IFP_ARPCU) FROM {{ this }} t)

    UNION

    -- BIPCU changes
    SELECT DISTINCT CUCOMP, CUACCT
    FROM {{ ref('ODS_IFP_BIPCU') }}
    WHERE ODS_UPDATE_TIMESTAMP > (SELECT MAX(t.UPDATE_TIMESTAMP_ODS_IFP_BIPCU) FROM {{ this }} t)

    UNION

    -- BIPIF changes
    SELECT DISTINCT IFCOMP, IFACCT
    FROM {{ ref('ODS_IFP_BIPIF') }}
    WHERE ODS_UPDATE_TIMESTAMP > (SELECT MAX(t.UPDATE_TIMESTAMP_ODS_IFP_BIPIF) FROM {{ this }} t)

    UNION

    -- ARPCC changes
    SELECT DISTINCT CCCO, CCCUNO
    FROM {{ ref('ODS_IFP_ARPCC') }}
    WHERE ODS_UPDATE_TIMESTAMP > (SELECT MAX(t.UPDATE_TIMESTAMP_ODS_IFP_ARPCC) FROM {{ this }} t)

    UNION

    -- CDPARTB changes
    SELECT DISTINCT Company, Account
    FROM {{ ref('ODS_IFP_CDPARTB') }}
    WHERE ODS_UPDATE_TIMESTAMP > (SELECT MAX(t.UPDATE_TIMESTAMP_ODS_IFP_CDPARTB) FROM {{ this }} t)

    UNION

    -- CUPCST01 changes
    SELECT DISTINCT CST_COMP, CST_ACCT
    FROM {{ ref('ODS_IFP_CUPCST01') }}
    WHERE ODS_UPDATE_TIMESTAMP > (SELECT MAX(t.UPDATE_TIMESTAMP_ODS_IFP_CUPCST01) FROM {{ this }} t)

    UNION

    -- BIPIFE changes
    SELECT DISTINCT FECOMP, FEACCT
    FROM {{ ref('ODS_IFP_BIPIFE') }}
    WHERE ODS_UPDATE_TIMESTAMP > (SELECT MAX(t.UPDATE_TIMESTAMP_ODS_IFP_BIPIFE) FROM {{ this }} t)

    UNION

    -- CUPCUA changes (account group)
    SELECT DISTINCT UACONO, UAACCT
    FROM {{ ref('ODS_IFP_CUPCUA') }}
    WHERE ODS_UPDATE_TIMESTAMP > (SELECT MAX(t.UPDATE_TIMESTAMP_ODS_IFP_CUPCUA) FROM {{ this }} t)

    UNION

    -- CUPCUG changes (account group detail)
    SELECT DISTINCT CUPCUA.UACONO, CUPCUA.UAACCT
    FROM {{ ref('ODS_IFP_CUPCUG') }} CUPCUG
    INNER JOIN {{ ref('ODS_IFP_CUPCUA') }} CUPCUA ON CUPCUG.UGACGRPID = CUPCUA.UAACGRPID
    WHERE CUPCUG.ODS_UPDATE_TIMESTAMP > (SELECT MAX(t.UPDATE_TIMESTAMP_ODS_IFP_CUPCUG) FROM {{ this }} t)

    UNION

    -- BIPAS changes
    SELECT DISTINCT ASCOMP, ASACCT
    FROM {{ ref('ODS_IFP_BIPAS') }}
    WHERE ODS_UPDATE_TIMESTAMP > (SELECT MAX(t.UPDATE_TIMESTAMP_ODS_IFP_BIPAS) FROM {{ this }} t WHERE t.UPDATE_TIMESTAMP_ODS_IFP_BIPAS IS NOT NULL)

    UNION

    -- SFDC changes – resolve back to ARPCU keys via the join logic
    SELECT DISTINCT ARPCU.CUCO, ARPCU.CUCUNO
    FROM {{ ref('ODS_SFDC_ACCOUNT') }} SFDC
    INNER JOIN {{ ref('ODS_IFP_ARPCU') }} ARPCU
        ON ARPCU.CUCO = SFDC.INFOPRO_NUMBER__C
        AND LPAD(ARPCU.CUCUNO, 7, '0') = LPAD(TRIM(SPLIT_PART(SFDC.INFOPRO_ACCOUNT_NUMBER__C, '/',2)), 7, '0')
    WHERE SFDC.ODS_UPDATE_TIMESTAMP > (SELECT MAX(t.UPDATE_TIMESTAMP_ODS_SFDC_ACCOUNT) FROM {{ this }} t)
      AND SFDC.ISDELETED = 0 AND SFDC.INFOPRO_NUMBER__C IS NOT NULL AND SFDC.ACCOUNTNUMBER IS NOT NULL

    UNION

    -- ARPCR changes – resolve back to account keys via ARPCC
    SELECT DISTINCT ARPCC.CCCO, ARPCC.CCCUNO
    FROM {{ ref('ODS_IFP_ARPCR') }} ARPCR
    INNER JOIN {{ ref('ODS_IFP_ARPCC') }} ARPCC ON ARPCC.CCCRPC = ARPCR.CRCRPC
    WHERE ARPCR.ODS_UPDATE_TIMESTAMP > (SELECT MAX(t.UPDATE_TIMESTAMP_ODS_IFP_ARPCR) FROM {{ this }} t)
)
{% endif %}

