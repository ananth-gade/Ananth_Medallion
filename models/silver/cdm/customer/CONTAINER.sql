,CTE_CDH_ASSET AS (
	SELECT
		apsr_sfdc.PK_System_Relational_Key AS CONTAINER_SFDC_NUMBER,
        CASE WHEN ap.PK_AssetProfile_ID IS NULL THEN 0 ELSE ap.PK_AssetProfile_ID END AS ASSET_PROFILE_NUMBER,
		SUBSTRING(apsr_ifp.PK_System_Relational_Key,1,3) AS InfoproDiv,
		CAST(CAST(SUBSTRING(apsr_ifp.PK_System_Relational_Key,4,7) AS INTEGER) AS VARCHAR(14)) AS Acct_Nbr,
		SUBSTRING(apsr_ifp.PK_System_Relational_Key,11,5) AS Site_Nbr,
		SUBSTRING(apsr_ifp.PK_System_Relational_Key,16,2) AS CTGR_Nbr,
		ap.ODS_INSERT_TIMESTAMP AS INSERT_TIMESTAMP_ASSETPROFILE,
		apsr_ifp.ODS_INSERT_TIMESTAMP AS INSERT_TIMESTAMP_ASSET_SYS_REL_IFP,
		apsr_sfdc.ODS_INSERT_TIMESTAMP AS INSERT_TIMESTAMP_ASSET_SYS_REL_SFDC,
		ap.ODS_UPDATE_TIMESTAMP AS UPDATE_TIMESTAMP_ASSETPROFILE,	
		apsr_ifp.ODS_UPDATE_TIMESTAMP AS UPDATE_TIMESTAMP_ASSET_SYS_REL_IFP,
		apsr_sfdc.ODS_UPDATE_TIMESTAMP AS UPDATE_TIMESTAMP_ASSET_SYS_REL_SFDC,
		ap.ODS_CDH_ASSETPROFILE_PK AS ODS_CDH_ASSETPROFILE_PK,
		apsr_ifp.ODS_CDH_ASSETPROFILE_SYSTEM_RELATIONSHIP_PK AS ODS_CDH_ASSETPROFILE_SYSTEM_RELATIONSHIP_PK,
		(SELECT ODS_CDH_SYSTEM_PK FROM {{ ref('ODS_CDH_SYSTEM') }} WHERE SYSTEM_NAME = 'Infopro' AND ACTIVE_RECORD_FLAG = 'Y'  LIMIT 1) AS ODS_CDH_SYSTEM_PK,
		(SELECT ODS_INSERT_TIMESTAMP FROM {{ ref('ODS_CDH_SYSTEM') }} WHERE SYSTEM_NAME = 'Infopro' AND ACTIVE_RECORD_FLAG = 'Y' LIMIT 1) AS INSERT_TIMESTAMP_SYSTEM,
        (SELECT ODS_UPDATE_TIMESTAMP FROM {{ ref('ODS_CDH_SYSTEM') }} WHERE SYSTEM_NAME = 'Infopro' AND ACTIVE_RECORD_FLAG = 'Y' LIMIT 1) AS UPDATE_TIMESTAMP_SYSTEM
	FROM {{ ref('ODS_CDH_ASSETPROFILE') }} ap
	LEFT JOIN {{ ref('ODS_CDH_ASSETPROFILE_SYSTEM_RELATIONSHIP') }} apsr_ifp
		ON ap.PK_AssetProfile_ID = apsr_ifp.PK_AssetProfile_ID
		AND apsr_ifp.PK_SYSTEM_ID = (
			SELECT PK_SYSTEM_ID FROM {{ ref('ODS_CDH_SYSTEM') }} cdh_sys
			WHERE SYSTEM_NAME = 'Infopro' AND ACTIVE_RECORD_FLAG = 'Y' --AND CURRENT_IND = 'Y'
		)
		AND apsr_ifp.ACTIVE_RECORD_FLAG = 'Y'
		--AND apsr_ifp.CURRENT_IND = 'Y' , column CURRENT_IND is not exist
	LEFT JOIN {{ ref('ODS_CDH_ASSETPROFILE_SYSTEM_RELATIONSHIP') }} apsr_sfdc
		ON ap.PK_AssetProfile_ID = apsr_sfdc.PK_AssetProfile_ID
		AND apsr_sfdc.PK_SYSTEM_ID = (
			SELECT PK_SYSTEM_ID
			FROM {{ ref('ODS_CDH_SYSTEM') }} 
			WHERE SYSTEM_NAME = 'Salesforce' AND ACTIVE_RECORD_FLAG = 'Y' --AND CURRENT_IND = 'Y'
		)
		AND apsr_sfdc.ACTIVE_RECORD_FLAG = 'Y'
		--AND apsr_sfdc.CURRENT_IND = 'Y'
	WHERE ap.ACTIVE_RECORD_FLAG = 'Y'
)

