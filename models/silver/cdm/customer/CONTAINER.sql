,CTE_TRANSFORMATION_CONTAINER AS (
		
	SELECT	
    -------------------- BIPAC columns --------------------

	bipac.ACCOMP AS INFOPRO_DIVISION_NUMBER,
	bipac.ACACCT AS ACCOUNT_NUMBER,
	bipac.ACSITE AS SITE_NUMBER,
	bipac.ACCTGR AS CONTAINER_GROUP_NUMBER,
	bipac.ACREVD AS REVENUE_DISTRIBUTION_CODE,
	CONCAT(bipac.ACCOMP, '-', LPAD(bipac.ACACCT,7,'0'), '-', bipac.ACSITE, '-', LPAD(bipac.ACCTGR,2,'0')) AS CONTAINER_COMPOSITE_KEY,
	CONCAT(bipac.ACCOMP, '-', LPAD(bipac.ACACCT,7,'0'), '-', bipac.ACSITE) AS SITE_COMPOSITE_KEY,
	CONCAT(bipac.ACCOMP, '-', LPAD(bipac.ACACCT,7,'0')) AS ACCOUNT_COMPOSITE_KEY,
	CASE WHEN bipac.ACCNID IS NOT NULL AND TRIM(bipac.ACCNID) <> '' THEN 1 ELSE 0 END AS IS_SHARED,
    bipac.ACTOTL AS PICKUP_PERIOD_TOTAL_LIFTS,
    bipac.ACPLTH AS PICKUP_PERIOD_LENGTH,
	CASE 
		WHEN bipac.ACOCAL = 'Y' THEN 'OnCall' 
		WHEN bipac.ACTOTL = 0 OR bipac.ACPLTH = 0 THEN 'None' 
		WHEN bipac.ACPLTH = 52 THEN CAST(bipac.ACTOTL AS VARCHAR) || ' Per Year' 
		WHEN bipac.ACPLTH = 1 THEN CAST(bipac.ACTOTL AS VARCHAR) || ' Every Week' 
		WHEN bipac.ACPLTH = 4 THEN CAST(bipac.ACTOTL AS VARCHAR) || ' Per Month' 
		ELSE CAST(bipac.ACTOTL AS VARCHAR) || ' Every ' || CAST(bipac.ACPLTH AS VARCHAR) || ' Weeks' 
	END AS LIFT_SCHEDULE_DESCRIPTION,
	bipac.ACCGRP AS CONTRACT_GROUP_NUMBER,
	bipac.ACMFCT AS MUNICIPAL_FRANCHISE_CONTRACT,
	bipac.ACGRID AS GRID_NUMBER,
	CASE WHEN TRIM(ACTYPE) = '' THEN 'P' ELSE ACTYPE END AS ACCOUNT_TYPE,
	bipac.ACRCFQ AS RECURRING_CHARGE_FREQUENCY,
	bipac.ACAVRC AS RECURRING_MONTHS_ADV_BILL,
	bipac.ACAVSV AS SERVICE_MONTHS_ADV_BILL,
	bipac.ACRATT AS RATE_TYPE,
	bipac.ACSTCD AS STOP_CODE,
	bipac.ACDIST AS DISTRICT_CODE,
	CASE WHEN bipac.ACDBIL = 'Y' THEN 1 ELSE 0 END AS IS_DISPOSAL_BILLABLE,
	CASE WHEN bipac.ACSDBL = 'Y' THEN 1 ELSE 0 END AS IS_STD_DISPOSAL_BILLABLE,
	bipac.ACLFCD AS DISPOSAL_CODE,
	bipac.ACLFPC AS DISPOSAL_PRICE_CODE,
	CASE WHEN bipac.ACLRAT IS NULL THEN 0 ELSE bipac.ACLRAT END AS DISPOSAL_RATE_PER_UNIT,
	CASE WHEN bipac.ACADPC IS NULL THEN 0 ELSE bipac.ACADPC END AS DISPOSAL_ADJUSTMENT_VALUE,
	CASE WHEN bipac.ACADFT IS NULL THEN 0 ELSE bipac.ACADFT END AS DISPOSAL_DOLLAR_ADJUSTMENT,
	CASE WHEN bipac.ACADFP IS NULL THEN '0' ELSE bipac.ACADFP END AS DISPOSAL_ADJUSTMENT_CODE,
	CASE WHEN bipac.ACCRCD IS NULL THEN '0' ELSE bipac.ACCRCD END AS DISPOSAL_COST_PER_REVENUE_CODE,
	CASE WHEN bipac.ACDTAC IS NULL THEN '0' ELSE bipac.ACDTAC END AS DISPOSAL_TAX_APPLICATION_CODE,
	CASE
	  -- Step 1: Check special handling flag against lookup table
		WHEN bipac.ACSHFL IN ('#', '1', '6', 'E', 'F', 'M', 'N', 'U') THEN 'Solid Waste'  -- MSW
		WHEN bipac.ACSHFL IN ('!', '�', '0', '2', '3', '4', '7', '8', '9', 'B', 'C', 'G', 'I', 'J', 'K', 'L', 'O', 'P', 'Q', 'R', 'T', 'V', 'Y', 'Z') THEN 'Recycle'  -- REC
	  	WHEN bipac.ACSHFL IN ('%', '&', '*', '@', '5', 'W') THEN 'Other'  -- C&D/Other
	  	WHEN bipac.ACSHFL IN ('A', 'D', 'H', 'S', 'X') THEN 'Other'  -- IND/Other
	  -- Step 2: If ACSHFL is blank/null, check container type
	  	WHEN (bipac.ACSHFL IS NULL OR bipac.ACSHFL = ' ')
	    		AND bipac.ACCTTP IN ('RC', 'BB', 'RB', 'FC', 'FR', 'YC') THEN 'Recycle'  -- Recycle container types
	  	WHEN (bipac.ACSHFL IS NULL OR bipac.ACSHFL = ' ')
	    		AND bipac.ACCTTP = 'YC' THEN 'Yard Waste'  -- Yard waste container
	  	WHEN (bipac.ACSHFL IS NULL OR bipac.ACSHFL = ' ')
	    		AND bipac.ACCTTP = 'OC' THEN 'Organics'  -- Organics container
	  -- Step 3: If still no match, check revenue distribution code 2nd digit
	  	WHEN SUBSTRING(bipac.ACREVD, 2, 1) = '7' THEN 'Recycle'
	  	-- Default: Solid Waste
	  	ELSE 'Solid Waste'
	END AS WASTE_STREAM_DESCRIPTION,
	bipac.ACSHFL AS SPECIAL_HANDLING_CODE,
	CAST(CASE WHEN (CASE WHEN bipacc.ACFG04 IS NULL THEN '' ELSE bipacc.ACFG04 END) = 'F' THEN TRUE ELSE FALSE END AS VARCHAR) AS FRANCHISE_FLAG,
	CAST(CASE WHEN bipac.ACPOFG IS NULL THEN 0 WHEN bipac.ACPOFG = 'Y' THEN 1 ELSE 0 END AS INT) AS IS_PURCHASE_ORDER_REQUIRED,
	CASE WHEN bipac.ACRCPT IS NULL THEN '' ELSE bipac.ACRCPT END AS RECEIPT_REQUIRED_CODE,
	CAST(CASE WHEN bipac.ACCNFL IS NULL THEN 0 WHEN bipac.ACCNFL = 'Y' THEN 1 ELSE 0 END AS INT) AS HAS_CONTAINER_NOTES,
	CAST(CASE WHEN bipac.ACENDT > CURRENT_DATE THEN 1 ELSE 0 END AS INT) AS IS_ACTIVE,
	CAST(TRY_TO_DATE(TO_VARCHAR(bipac.ACORDT)) AS DATETIME) AS ORIGINAL_START_DATE,
	CAST(TRY_TO_DATE(TO_VARCHAR(bipac.ACSTDT)) AS DATE) AS LAST_SERVICE_DATE,
	CAST(TRY_TO_DATE(TO_VARCHAR(bipac.ACRMDT)) AS DATE) AS REMOVAL_DATE,
	CAST(TRY_TO_DATE(TO_VARCHAR(bipac.ACENDT)) AS DATETIME) AS CLOSE_DATE,
	CAST(TRY_TO_DATE(TO_VARCHAR(bipac.ACRODT)) AS DATETIME) AS RATE_RESTRICTION_DATE,
	CASE WHEN bipac.ACDRDT<>'0' AND TRY_TO_DATE(TO_VARCHAR(bipac.ACDRDT)) IS NOT NULL THEN CAST(TRY_TO_DATE(TO_VARCHAR(bipac.ACDRDT)) AS DATE) ELSE CAST(TRY_TO_DATE('9999-12-31') AS DATE) END AS DISPOSAL_RATE_RESTRICTION_DATE,
	CAST(bipac.ACREDT AS DATE) AS NEXT_RECURRING_CHARGE_GENERATION_DATE,
	CAST(bipac.ACBTDT AS DATE) AS BILLED_TO_DATE,
	CAST(TRY_TO_DATE(TO_VARCHAR(bipac.ACSSDT)) AS DATETIME) AS SEASONAL_STOP_DATE,
	CAST(TRY_TO_DATE(TO_VARCHAR(bipac.ACSEDT)) AS DATETIME) AS SEASONAL_RESTART_DATE,
	CAST(CASE WHEN bipac.ACRMRT = 'Y' THEN 1 ELSE 0 END AS INT) AS IS_SEASONAL_REMOVE_ROUTE,
	CAST(CASE WHEN bipac.ACRMCT = 'Y' THEN 1 ELSE 0 END AS INT) AS IS_SEASONAL_REMOVE_CONTAINER,
	CAST(CASE WHEN bipac.ACSTRC = 'Y' THEN 1 ELSE 0 END AS INT) AS IS_SEASONAL_RECURRING_CHARGE,
	bipac.ACCTTP AS CONTAINER_TYPE,
	bipac.ACCTSZ AS CONTAINER_SIZE,
	bipac.ACCNID AS SHARED_CONTAINER_ID,
	CASE WHEN bipac.ACPUNT = 'W' THEN 'WEEK' ELSE 'UNKNOWN' END AS PICKUP_PERIOD_UNIT,
	CASE WHEN bipac.ACPLTH = 1 THEN 'WEEKLY' WHEN bipac.ACPLTH = 2 THEN 'EOW' WHEN bipac.ACPLTH = 4 THEN 'MONTHLY' WHEN bipac.ACPLTH = 0 THEN 'ON CALL' ELSE 'OTHER' END AS PICKUP_SCHEDULE,
	CAST(bipac.ACMINL AS DECIMAL) AS MINIMUM_NUMBER_OF_LIFTS,
	CASE WHEN bipac.ACMOND IS NULL THEN 0 ELSE bipac.ACMOND END AS MONDAY_LIFT_DAY,
	CASE WHEN bipac.ACTUES IS NULL THEN 0 ELSE bipac.ACTUES END AS TUESDAY_LIFT_DAY,
	CASE WHEN bipac.ACWEDN IS NULL THEN 0 ELSE bipac.ACWEDN END AS WEDNESDAY_LIFT_DAY,
	CASE WHEN bipac.ACTHUR IS NULL THEN 0 ELSE bipac.ACTHUR END AS THURSDAY_LIFT_DAY,
	CASE WHEN bipac.ACFRID IS NULL THEN 0 ELSE bipac.ACFRID END AS FRIDAY_LIFT_DAY,
	CASE WHEN bipac.ACSATU IS NULL THEN 0 ELSE bipac.ACSATU END AS SATURDAY_LIFT_DAY,
	CASE WHEN bipac.ACSUND IS NULL THEN 0 ELSE bipac.ACSUND END AS SUNDAY_LIFT_DAY,
	CAST(CASE WHEN (CASE WHEN bipac.ACMORQ IS NULL THEN '' ELSE bipac.ACMORQ END) = 'Y' THEN 1 ELSE 0 END AS INT) AS IS_MONDAY_REQUIRED,
	CAST(CASE WHEN (CASE WHEN bipac.ACTURQ IS NULL THEN '' ELSE bipac.ACTURQ END) = 'Y' THEN 1 ELSE 0 END AS INT) AS IS_TUESDAY_REQUIRED,
	CAST(CASE WHEN (CASE WHEN bipac.ACWERQ IS NULL THEN '' ELSE bipac.ACWERQ END) = 'Y' THEN 1 ELSE 0 END AS INT) AS IS_WEDNESDAY_REQUIRED,
	CAST(CASE WHEN (CASE WHEN bipac.ACTHRQ IS NULL THEN '' ELSE bipac.ACTHRQ END) = 'Y' THEN 1 ELSE 0 END AS INT) AS IS_THURSDAY_REQUIRED,
	CAST(CASE WHEN (CASE WHEN bipac.ACFRRQ IS NULL THEN '' ELSE bipac.ACFRRQ END) = 'Y' THEN 1 ELSE 0 END AS INT) AS IS_FRIDAY_REQUIRED,
	CAST(CASE WHEN (CASE WHEN bipac.ACSARQ IS NULL THEN '' ELSE bipac.ACSARQ END) = 'Y' THEN 1 ELSE 0 END AS INT) AS IS_SATURDAY_REQUIRED,
	CAST(CASE WHEN (CASE WHEN bipac.ACSURQ IS NULL THEN '' ELSE bipac.ACSURQ END) = 'Y' THEN 1 ELSE 0 END AS INT) AS IS_SUNDAY_REQUIRED,
	CASE WHEN bipac.ACQTYC IS NULL THEN 0 ELSE bipac.ACQTYC END AS CONTAINER_COUNT,
	CASE WHEN bipac.ACQTYS IS NULL THEN 0 ELSE bipac.ACQTYS END AS CONTAINER_QUANTITY_ON_SITE,
	CASE WHEN (CASE WHEN bipac.ACPLTH IS NULL THEN 0 ELSE bipac.ACPLTH END) = 0 THEN 0 ELSE (((bipac.ACTOTL / bipac.ACPLTH) * bipac.ACCTSZ) * 52) / 12 END AS YARDAGE,
	CASE WHEN bipac.ACOLDN IS NULL THEN '0' ELSE bipac.ACOLDN END AS UNIT_OF_MEASURE,
	CAST(CASE WHEN bipac.ACCPAC = 'C' THEN 1 ELSE 0 END AS INT) AS HAS_COMPACTOR,
	CAST(CASE WHEN bipac.ACCOWN = 'Y' THEN 1 ELSE 0 END AS INT) AS IS_CONTAINER_OWNED,
	CAST(CASE WHEN bipac.ACOCAL = 'Y' THEN 1 ELSE 0 END AS INT) AS IS_ONCALL,
    CAST(CASE WHEN COALESCE(bipac.ACMUNP, '') = 'Y' THEN TRUE ELSE FALSE END AS VARCHAR) AS MUNICIPAL_FLAG,


    -------------------- BIPACC columns --------------------

	bipacc.ACAVGW AS EXCESS_TON_AMOUNT,
	CAST(bipacc.ACAVGW AS DECIMAL) AS AVERAGE_WEIGHT,
	bipacc.ACCITY AS CITY_ACCOUNT_NUMBER,
	CASE WHEN bipacc.ACFG05 IS NULL THEN '' ELSE bipacc.ACFG05 END AS MARKET_VERTICAL_INDICATOR,
	cuptb_mvt.TBDESC AS MARKET_DESCRIPTION,
    bipacc.ACFG03 AS REMOTE_MONITOR_FLAG,
    bipacc.ACCOMM AS COMMODITY_CODE,

    -------------------- BIPCG columns --------------------

    bipcg.CGGNAM AS MUNICIPAL_CONTRACT_GROUP_NAME,
    bipcg.CGSUMR AS SUMMARY_ROUTING_FLAG,
    bipcg_ind.CGINDA AS INDIVIDUAL_ACCOUNT_FLAG,
    bipcg_ind.CGINIR AS INDIVIDUAL_INVOICES_REQUIRED_FLAG,
	bipcg.CGSVC1 AS DIGITAL_COMMUNICATIONS_FLAG,

    -------------------- BIPCM columns --------------------

    bipcm.CMCTTY AS MUNICIPAL_CONTRACT_TYPE,

    -------------------- BIPTB columns --------------------

    biptb.TBDESC AS CONTAINER_NAME,

    -------------------- CUPASSACCT columns --------------------

    CAST(cupassacct.ASAASCD AS VARCHAR(255)) AS ASSOCIATION_CODE,

	-------------------- CUPASSCODE columns --------------------

